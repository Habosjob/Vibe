from pathlib import Path

# Корневая директория проекта: определяется автоматически как папка,
# в которой находится файл config.py. Это базовый путь, от которого
# строятся все остальные папки и файлы проекта.
PROJECT_ROOT: Path = Path(__file__).resolve().parent

# Папка с документацией. Здесь можно хранить дополнительные инструкции,
# описания процессов и любые справочные материалы.
DOCS_DIR: Path = PROJECT_ROOT / "docs"

# Папка для кэша. Используется для временных данных, которые можно
# безопасно пересоздать при следующем запуске.
CACHE_DIR: Path = PROJECT_ROOT / "cache"

# Папка для базы данных. На следующих этапах здесь будет храниться SQLite
# файл и связанные с ним служебные файлы.
DB_DIR: Path = PROJECT_ROOT / "db"

# Папка для итоговых результатов. Сюда сохраняются финальные выходные
# файлы, например Excel-отчёты или сводные таблицы.
OUTPUT_DIR: Path = PROJECT_ROOT / "output"

# Папка для «сырых» данных. Здесь размещаются исходные выгрузки до
# обработки и фильтрации.
RAW_DIR: Path = PROJECT_ROOT / "raw"

# Папка для логов приложения. На следующих этапах здесь будут храниться
# журналы выполнения скрипта.
LOGS_DIR: Path = PROJECT_ROOT / "logs"

# Список всех рабочих директорий, которые должны существовать при старте.
# main.py использует этот список для автоматического создания папок.
REQUIRED_DIRS: tuple[Path, ...] = (
    DOCS_DIR,
    CACHE_DIR,
    DB_DIR,
    OUTPUT_DIR,
    RAW_DIR,
    LOGS_DIR,
)

# Таймаут подключения к внешним источникам данных в секундах.
# Если источник не отвечает дольше этого времени, попытка считается
# неудачной и может быть повторена по правилам ретраев.
CONNECT_TIMEOUT_SEC: float = 10.0

# Таймаут ожидания ответа после успешного подключения в секундах.
# Нужен для защиты от «зависших» соединений.
READ_TIMEOUT_SEC: float = 30.0

# Максимальное количество попыток повтора сетевого запроса.
# Используется при временных ошибках связи или ответах 5xx.
MAX_RETRIES: int = 3

# Базовая пауза между повторами в секундах.
# На следующих этапах может использоваться в формуле экспоненциального
# backoff (например, 1, 2, 4 секунды).
RETRY_BACKOFF_BASE_SEC: float = 1.0

# Ограничение параллельности для операций ввода-вывода.
# Позволяет ускорять выполнение, но не перегружать сеть и источник данных.
MAX_CONCURRENT_REQUESTS: int = 5

# Время жизни файлов кэша в часах.
# После истечения этого срока кэш считается устаревшим и подлежит
# обновлению.
CACHE_TTL_HOURS: int = 24

# Имя файла состояния (чекпоинта) в папке cache.
# Нужен для возобновления работы после прерывания на следующих этапах.
STATE_FILE_NAME: str = "state.json"

# Полный путь к файлу состояния в папке cache.
STATE_FILE_PATH: Path = CACHE_DIR / STATE_FILE_NAME

# Имя основного файла базы данных SQLite.
DB_FILE_NAME: str = "bonds.sqlite3"

# Полный путь к файлу базы данных.
DB_FILE_PATH: Path = DB_DIR / DB_FILE_NAME

# Таймаут операций с базой данных SQLite в секундах.
# Влияет на то, как долго клиент ждёт освобождения блокировки.
SQLITE_TIMEOUT_SEC: float = 30.0

# Размер батча записи в базу данных.
# На следующих этапах поможет контролировать баланс между скоростью и
# надёжностью при массовой вставке записей.
DB_BATCH_SIZE: int = 500

# Имя итогового Excel-файла по умолчанию.
# Файл будет сохраняться в папку output.
OUTPUT_EXCEL_NAME: str = "bonds_screening_result.xlsx"

# Полный путь к итоговому Excel-файлу.
OUTPUT_EXCEL_PATH: Path = OUTPUT_DIR / OUTPUT_EXCEL_NAME

# Уровень логирования по умолчанию.
# Возможные значения: DEBUG, INFO, WARNING, ERROR, CRITICAL.
LOG_LEVEL: str = "INFO"

# Максимальный размер одного лог-файла в байтах перед ротацией.
# На следующих этапах можно использовать вместе с RotatingFileHandler.
LOG_MAX_BYTES: int = 1_000_000

# Количество резервных лог-файлов, которые сохраняются после ротации.
LOG_BACKUP_COUNT: int = 5

# Имя основного лог-файла.
LOG_FILE_NAME: str = "app.log"

# Полный путь к основному лог-файлу.
LOG_FILE_PATH: Path = LOGS_DIR / LOG_FILE_NAME
